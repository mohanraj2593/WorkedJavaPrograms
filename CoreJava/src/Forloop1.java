public class Forloop1
{
    public static void main(String[] args) {
        //0-10 print the even numbers
        for( int i=0;i<=10;i+=2)
        {
            System.out.println(i);
        }
        for(int i=1;i<=10;i+=2)
        {
            System.out.println(i);
        }
        for(int i=10;i>0;i-- )
        {
            System.out.println(i);
        }

    }

}
